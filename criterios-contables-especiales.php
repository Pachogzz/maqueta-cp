					    <p>En cumplimiento a lo previsto en el Criterio A-2 “Aplicación de normas particulares” referido en el artículo 195 de las Disposiciones de carácter general aplicables a las actividades de las sociedades cooperativas de ahorro y préstamo, criterio que a su vez remite a la NIF B-9 “Información financiera a fechas intermedias” de las Normas de Información Financiera emitidas por el Consejo Mexicano de Normas de Información Financiera, A. C., se revelan las siguientes notas a los estados financieros del mes de diciembre 2020.</p>
					    <p>El balance general contempla la aplicación de los “criterios contables especiales, aplicables a las sociedades cooperativas de ahorro y préstamo con niveles de operación I al IV, ante la contingencia del COVID-19” y al impacto negativo que está generando en diversas actividades de la economía, emitidos por la Comisión Nacional Bancaria y de Valores, el 23 de marzo de 2020 en el Diario Oficial de la Federación, con la intención de ofrecer a sus Socios flexibilidad de condiciones en materia de recuperación de créditos, mediante un programa de otorgamiento de plazo de espera para los préstamos que al 31 de marzo de 2020 hayan estado clasificados contablemente como vigentes, conforme a lo siguiente:</p>
					    <ol type="I">
							<li>En préstamos con pagos periódicos mensuales, el plazo de espera podrá ser de hasta por 6 meses.</li>
							<li>Para aquellos con pagos periódicos no mensuales y pagos únicos, cuya próxima fecha de pago se encuentre dentro del periodo de vigencia de este programa, el plazo de espera que se aplicará podrá ser hasta por un tiempo igual a la periodicidad de sus pagos, siempre y cuando dicho tiempo no sea mayor a 6 meses posteriores a la fecha originalmente programada de pago o 18 meses posteriores a la fecha de vencimiento, cuando se trate de créditos a pago único dirigidos al sector rural.</li>
							<li>Los pagos se reiniciarán una vez concluido el plazo de espera.</li>
							<li>El plazo de espera aplicará para el pago de capital e intereses.</li>
							<li>Los pagos no realizados se compensarán otorgando un plazo adicional a la fecha de vencimiento del préstamo, por el mismo número de pagos diferidos.</li>
							<li>El programa de apoyo terminó el 29 de julio de 2020.</li>
					    </ol>
					    <p>Los criterios contables que se debieron aplicar conforme a los criterios contables vigentes fueron los siguientes:</p>
