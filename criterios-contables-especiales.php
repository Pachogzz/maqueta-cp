							<li>Los créditos con pagos periódicos de principal e intereses, que sean objeto de reestructuración o renovación, se consideraron vigentes, sin que les aplicasen los requisitos establecidos en los párrafos 68 y 70 del Criterio B-4, según fuera el caso.</li>
							<li>Los créditos que desde su inicio se estipule su carácter de revolventes, que se reestructuren o renueven dentro de los 120 días naturales siguientes al 31 de marzo de 2020, no se consideraron como cartera vencida conforme a lo establecido en el párrafo 66 del Criterio B-4.</li>
							<li>La aplicación del programa de otorgamiento de plazo de espera no implicó quitas, condonaciones, bonificaciones, o descuentos sobre el saldo del crédito para repercutir en menores pagos para los Socios, como mecanismo para fortalecer la liquidez de estos últimos, por lo que no se difirió la constitución de estimaciones preventivas para riesgos crediticios y se aplicó lo establecido en el Anexo C de las Disposiciones.</li>
