						<p class="lead font-weight-bolder">Tenemos el compromiso de potenciar las competencias esenciales en los jóvenes ahorradores para que tengan un papel activo tanto en la sociedad como en la cooperativa.</p>

					    <p>Más allá de los productos o servicios que una cooperativa pone a disposición de su comunidad, está tiene la responsabilidad de realizar una constante así como eficiente labor educativa que le permita nacer, crecer y consolidarse como una empresa con valor humano entre los socios, dirigentes, colaboradores y principalmente entre los jóvenes que la integran.</p>

					    <p>El Encuentro Nacional de Jóvenes Ahorradores, Generacción #Joven que realiza anualmente Caja Popular Mexicana a través de la gerencia de Educación Cooperativa, es justamente la oportunidad que tenemos como institución de sembrar en las nuevas generaciones los valores que por más de 66 años han acompañado el crecimiento de las cajas populares en México; es la ocasión de sensibilizar así como educar en el valor de la ayuda mutua a los futuros padres de familia, empresarios, directores y comerciantes para la construcción de una mejor sociedad.</p>

					    <p>Durante la edición número once realizada del 5 al 7 de octubre en Amealco de Bonfil, Querétaro., con conferencias magistrales, eventos culturales, musicales, recreativos, lúdicos así como de conciencia social, pusimos en práctica el quinto principio del cooperativismo universal: “Educación, capacitación e información” entre los más de 140 jóvenes elegidos por su desempeño entre más de cinco mil aspirantes de todo el país.</p>

					    <p>Nos alegra promover el emprendurismo; es por esto que durante el evento impulsamos la sexta edición del reto: “Misión Posible” que tiene el objetivo reconocer ideas creativas e innovadoras que generen proyectos sustentables en las regiones de procedencia de los participantes. Este año los ganadores del primer lugar con el proyecto “Yol Té” de jabones de avena y miel fueron los integrantes de la región de Tala – Guadalajara a quienes se les otorgo la cantidad de ocho mil pesos para invertirlos en el proyecto, el segundo lugar fue para la región de Celaya con un proyecto de “abrigos” así como cinco mil pesos y el tercer lugar la región de Tampico con un proyecto de energía renovable así como el apoyo económico de tres mil pesos.</p>

					    <p>Uno de los momentos más emotivos así como de mayor aprendizaje para los jóvenes, fue la actividad “Dar te dignifica”, donde con la entrega del donativo en especie que recolectaron las plazas se apoyó a la “Casa Otomí” que brinda ayuda a las comunidades indígenas más vulnerables del estado de Querétaro.</p>

					    <p>El Encuentro Nacional de Jóvenes Ahorradores, Generacción #Joven fue también la oportunidad de celebrar el Día del Joven Cooperativista donde anualmente en Caja Popular Mexicana reconocemos la importancia de sumar la visión de las nuevas generaciones a los proyectos de la cooperativa de ahorro y préstamo más grande de México.</p>

					    <p>En Caja Popular Mexicana tenemos un compromiso con los jóvenes así como con la educación, es por ello que destinamos recursos que permitan a los ahorradores desarrollar conductas así como hábitos positivos de comportamiento a la vez que los prepare para incursionar en el movimiento cooperativo mexicano.</p>

					    <h4>¡El presente y futuro de México!</h4>

					    <p>En su mensaje de bienvenida al evento Generacción #Joven 2017, Darío Ramsés Graneros Ruíz, presidente del Comité Juvenil Sinergia, recordó a los presentes las características que definen a los jóvenes de Caja Popular Mexicana, “tengamos siempre en mente que los jóvenes no se detienen ante nada, porque ser joven es luchar ante los retos, enfrentarlos y superarlos; es levantarnos con ese espíritu indomable que nos distingue”.</p>

					    <p>Por su parte, Carmen Laura Hernández Morales, directora general de la Federación Integradora Central, en su discurso inaugural exhorto a los presente a promover un estilo de vida cooperativista, “Más jóvenes necesita el movimiento cooperativo (…) ustedes son el futuro de esta nación, sigan aprendiendo y replicando las experiencias que aquí aprendan; estoy segura que quedarán para toda la vida”, finalizo.</p>

